#string validators are

a.isalnum()        # method to check characters of a string a
print(a.isalnum()) # returns True if all the characters are alphanumeric(0-9,a-z,A-Z)

a.isalpha()        # method to check characters of a string a
print(a.isalpha()) # returns True if all the characters are alphabetical(a-z,A-Z)

a.isdigit()        # method to check characters of a string a
print(a.isdigit()) # returns True if all the characters are digits(0-9)

a.islower()        # method to check characters of a string a
print(a.islower()) # returns True if all the characters are lowercase(a-z)

a.isupper()        # method to check characters of a string a
print(a.isupper()) # returns True if all the characters are uppercase(A-Z)
